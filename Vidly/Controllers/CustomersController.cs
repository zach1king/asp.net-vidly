using System.Collections.Generic;
using System.Data.Entity; //for ego loading
using System.Web.Mvc;
using System.Linq;
using Vidly.Models;
using Vidly.ViewModels;

namespace Vidly.Controllers
{
    public class CustomersController : Controller
    {
        private ApplicationDbContext _context;
        
        public CustomersController()
        {
            _context = new ApplicationDbContext();
        }

        protected override void Dispose(bool disposing)
        {
            _context.Dispose();
        }

        public ViewResult Index()
        {
            //var customers = GetCustomers();
            // _context.Customers ===> will only execute when looping through, ToList() will solve this
            var customers = _context.Customers.Include(c => c.MembershipType).ToList(); //ego loading
            // ego loading customers' MembershipType as well

            return View(customers);
        }

        public ActionResult Details(int id)
        {
            //var customer = GetCustomers().SingleOrDefault(c => c.Id == id);
            var customer = _context.Customers.Include(c => c.MembershipType).SingleOrDefault(c => c.Id == id); ;

            if (customer == null)
                return HttpNotFound();

            return View(customer);
        }

